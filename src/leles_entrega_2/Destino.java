package leles_entrega_2;

/**
 *
 * @author Juan
 */
public class Destino {
    String nomeLugar;
    String rua;
    String bairro;
    String numero;
    String cidade;
    String estado;

    public Destino(String nomeLugar, String rua, String bairro, String numero, String cidade, String estado) {
        this.nomeLugar = nomeLugar;
        this.rua = rua;
        this.bairro = bairro;
        this.numero = numero;
        this.cidade = cidade;
        this.estado = estado;
    }
}
